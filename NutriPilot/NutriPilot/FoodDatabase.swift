import Foundation


struct NutritionItem: Identifiable, Hashable {
    var id: String { name }
    let name: String
    let category: String
    let servingSize: String
    let calories: Int
    let protein: Double
    let carbs: Double
    let fat: Double
    let fiber: Double
    let isVegetarian: Bool
    let isHighProtein: Bool
    let isWeightLossFriendly: Bool
    let isMuscleGainFriendly: Bool

 
    var isVeg: Bool { isVegetarian }

    
    var asFoodItem: FoodItem {
        FoodItem(name: name, calories: calories, protein: protein,
                 carbs: carbs, fat: fat, fiber: fiber,
                 unit: .grams, baseAmount: servingSize)
    }
}

